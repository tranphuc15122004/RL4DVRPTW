#!/usr/bin/env bash
# ===========================================================================
# Paper Evaluation Script — AM & PolyNet on 5 DVRPTW datasets
#
# Evaluates 2 models × 5 datasets × 2 decode strategies (greedy, sample).
# Each (model, decode, dataset) combo is a separate invocation to ensure
# correct per-dataset parameters (e.g. PolyNet's k = customers_count + 1).
#
# Output structure:
#   output/paper_eval/
#   ├── am_greedy/
#   │   ├── dvrptw_n20m1_10240_am.json
#   │   ├── dvrptw_n50m3_10240_am.json
#   │   └── ...
#   ├── am_sample/
#   ├── polynet_greedy/
#   ├── polynet_sample/
#   ├── _logs/              # console output per run
#   ├── paper_summary.csv   # (generated by aggregate script)
#   ├── paper_comparison.csv
#   └── paper_stats.json
#
# Usage:
#   bash run_paper_eval.sh              # full evaluation
#   bash run_paper_eval.sh --dry-run    # print commands only
#   bash run_paper_eval.sh --quick      # n20m1 only (fast test)
#   bash run_paper_eval.sh --resume     # skip completed runs
#   bash run_paper_eval.sh --ds n100m5  # single dataset only
# ===========================================================================
set -euo pipefail

PYTHON_BIN="${PYTHON_BIN:-python3}"
ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$ROOT_DIR"

# ------------------------------------------------------------------
# Configurable defaults
# ------------------------------------------------------------------
RNG_SEED="${RNG_SEED:-42}"
NO_CUDA="${NO_CUDA:-0}"
VERIFY="${VERIFY:-1}"
STOCH_ROLLOUTS="${STOCH_ROLLOUTS:-100}"
PRINT_INSTANCES="${PRINT_INSTANCES:-0}"

# Paths
DATA_DIR="data"
OUTPUT_BASE="output/paper_eval"
LOG_DIR="${OUTPUT_BASE}/_logs"

AM_WEIGHT="data/_AM/chkpt_best.pyth"
AM_ARGS="data/_AM/args.json"
POLYNET_WEIGHT="data/_PolyNet/chkpt_best.pyth"
POLYNET_ARGS="data/_PolyNet/args.json"

# ------------------------------------------------------------------
# Parse CLI flags
# ------------------------------------------------------------------
DRY_RUN=false
QUICK_MODE=false
RESUME_MODE=false
SINGLE_DS=""

prev_arg=""
for arg in "$@"; do
    case "$arg" in
        --dry-run) DRY_RUN=true ;;
        --quick)   QUICK_MODE=true ;;
        --resume)  RESUME_MODE=true ;;
        --ds=*)    SINGLE_DS="${arg#--ds=}" ;;
        --ds)
            # handled below via prev_arg
            ;;
        *)
            if [[ "$prev_arg" == "--ds" ]]; then
                SINGLE_DS="$arg"
            else
                echo "Unknown flag: $arg"
                echo "Usage: bash run_paper_eval.sh [--dry-run] [--quick] [--resume] [--ds n100m5]"
                exit 1
            fi
            ;;
    esac
    prev_arg="$arg"
done

# ------------------------------------------------------------------
# Dataset definitions: tag | filename | customers | vehicles | capacity | speed
# ------------------------------------------------------------------
DATASETS=(
    "n20m1|dvrptw_n20m1_10240.pyth|20|1|200|1"
    "n50m3|dvrptw_n50m3_10240.pyth|50|3|200|1"
    "n100m5|dvrptw_n100m5_10240.pyth|100|5|200|1"
    "n200m10|dvrptw_n200m10_10240.pyth|200|10|200|1"
    "n400m20|dvrptw_n400m20_10240.pyth|400|20|200|1"
)

# ------------------------------------------------------------------
# Model configs
# ------------------------------------------------------------------
declare -A MODEL_WEIGHT_MAP
declare -A MODEL_ARGS_MAP

MODEL_WEIGHT_MAP["am"]="$AM_WEIGHT"
MODEL_ARGS_MAP["am"]="$AM_ARGS"
MODEL_WEIGHT_MAP["polynet"]="$POLYNET_WEIGHT"
MODEL_ARGS_MAP["polynet"]="$POLYNET_ARGS"

# ------------------------------------------------------------------
# Decode strategies
# ------------------------------------------------------------------
DECODES=("greedy" "sample")

# ------------------------------------------------------------------
# Helpers
# ------------------------------------------------------------------

banner() {
    echo ""
    echo "╔══════════════════════════════════════════════════════════════════╗"
    echo "║  $1"
    echo "╚══════════════════════════════════════════════════════════════════╝"
    echo ""
}

# Run a single (model, decode, dataset) evaluation
run_single() {
    local model="$1"
    local decode="$2"
    local ds_tag="$3"
    local ds_file="$4"
    local customers="$5"
    local vehicles="$6"
    local capacity="$7"
    local speed="$8"

    local output_dir="${OUTPUT_BASE}/${model}_${decode}"
    local model_weight="${MODEL_WEIGHT_MAP[$model]}"
    local model_args="${MODEL_ARGS_MAP[$model]}"
    local data_path="${DATA_DIR}/${ds_file}"

    local cli_args=(
        --model            "$model"
        --model-weight     "$model_weight"
        --model-args       "$model_args"
        --data-file        "$data_path"
        --output-dir       "$output_dir"
        --customers-count  "$customers"
        --vehicles-count   "$vehicles"
        --veh-capa         "$capacity"
        --veh-speed        "$speed"
        --rng-seed         "$RNG_SEED"
        --stoch-rollouts   "$STOCH_ROLLOUTS"
        --max-print-instances "$PRINT_INSTANCES"
    )

    # Decode flag
    if [[ "$decode" == "sample" ]]; then
        cli_args+=(--sample)
    else
        cli_args+=(--greedy)
    fi

    # CUDA
    if [[ "$NO_CUDA" == "1" ]]; then
        cli_args+=(--no-cuda)
    fi

    # Output file tag for resume check
    local file_tag="${ds_file%.pyth}"
    local expected_json="${output_dir}/${file_tag}_${model}.json"

    # Log
    local log_file="${LOG_DIR}/${model}_${decode}_${ds_tag}.log"
    mkdir -p "$LOG_DIR" "$output_dir"

    echo "  Dataset    : $ds_tag (n=$customers, m=$vehicles)"
    echo "  Model      : $model"
    echo "  Decode     : $decode"
    echo "  Output dir : $output_dir"
    echo "  Log        : $log_file"

    if $DRY_RUN; then
        echo "  CMD        : PYTHONPATH=. $PYTHON_BIN infer_batch.py ${cli_args[*]}"
        echo "  [DRY-RUN]"
        echo ""
        return 0
    fi

    # Resume check
    if $RESUME_MODE && [[ -f "$expected_json" ]]; then
        echo "  [RESUME] JSON exists, skipping."
        echo ""
        return 0
    fi

    # Execute
    echo "  Running..."
    PYTHONPATH=. "$PYTHON_BIN" infer_batch.py "${cli_args[@]}" 2>&1 | tee "$log_file"
    local exit_code=${PIPESTATUS[0]}

    if [[ $exit_code -ne 0 ]]; then
        echo ""
        echo "  ✗ FAILED (exit code $exit_code)"
        return $exit_code
    fi

    echo "  ✓ Done"
    echo ""
    return 0
}

# ------------------------------------------------------------------
# Count total tasks
# ------------------------------------------------------------------
count_tasks() {
    local n=0
    for model in "am" "polynet"; do
        for decode in "${DECODES[@]}"; do
            for ds_entry in "${DATASETS[@]}"; do
                IFS='|' read -r ds_tag _ _ _ _ _ <<< "$ds_entry"
                if [[ -n "$SINGLE_DS" && "$ds_tag" != "$SINGLE_DS" ]]; then
                    continue
                fi
                if $QUICK_MODE && [[ "$ds_tag" != "n20m1" ]]; then
                    continue
                fi
                n=$((n + 1))
            done
        done
    done
    echo $n
}

# ------------------------------------------------------------------
# Main
# ------------------------------------------------------------------

banner "PAPER EVALUATION — AM & PolyNet on 5 DVRPTW Datasets"

TOTAL=$(count_tasks)
echo "Models     : am, polynet"
echo "Decodes    : greedy, sample"
echo "Datasets   : ${#DATASETS[@]} files"
echo "Total tasks: $TOTAL (2 models × 2 decodes × ${#DATASETS[@]} datasets)"
echo "Seed       : $RNG_SEED"
echo "Output     : $OUTPUT_BASE"
echo ""

if $QUICK_MODE; then
    echo "⚡ QUICK MODE — n20m1 only"
    echo ""
fi
if [[ -n "$SINGLE_DS" ]]; then
    echo "🎯 SINGLE DATASET — $SINGLE_DS only"
    echo ""
fi
if $DRY_RUN; then
    echo "🔍 DRY-RUN — printing commands only"
    echo ""
fi

# Check prerequisites
if ! $DRY_RUN; then
    if [[ ! -f "$AM_WEIGHT" ]]; then
        echo "ERROR: AM checkpoint not found: $AM_WEIGHT"
        exit 1
    fi
    if [[ ! -f "$POLYNET_WEIGHT" ]]; then
        echo "ERROR: PolyNet checkpoint not found: $POLYNET_WEIGHT"
        exit 1
    fi
    for ds_entry in "${DATASETS[@]}"; do
        IFS='|' read -r ds_tag ds_file _ _ _ _ <<< "$ds_entry"
        if [[ -n "$SINGLE_DS" && "$ds_tag" != "$SINGLE_DS" ]]; then
            continue
        fi
        if [[ ! -f "${DATA_DIR}/${ds_file}" ]]; then
            echo "ERROR: Dataset not found: ${DATA_DIR}/${ds_file}"
            exit 1
        fi
    done
    echo "✓ All checkpoints and datasets found."
    echo ""
fi

# Run all combinations
FAILED=()
TASK_N=0
START_TS=$(date +%s)

for model in "am" "polynet"; do
    for decode in "${DECODES[@]}"; do
        for ds_entry in "${DATASETS[@]}"; do
            IFS='|' read -r ds_tag ds_file customers vehicles capacity speed <<< "$ds_entry"

            # Filtering
            if [[ -n "$SINGLE_DS" && "$ds_tag" != "$SINGLE_DS" ]]; then
                continue
            fi
            if $QUICK_MODE && [[ "$ds_tag" != "n20m1" ]]; then
                continue
            fi

            TASK_N=$((TASK_N + 1))
            echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
            echo "[$TASK_N/$TOTAL] ${model^^} + ${decode} + ${ds_tag}"
            echo ""

            if ! run_single "$model" "$decode" "$ds_tag" "$ds_file" \
                 "$customers" "$vehicles" "$capacity" "$speed"; then
                FAILED+=("${model}_${decode}_${ds_tag}")
            fi
        done
    done
done

END_TS=$(date +%s)
ELAPSED=$((END_TS - START_TS))
ELAPSED_FMT=$(printf '%02d:%02d:%02d' $((ELAPSED/3600)) $((ELAPSED%3600/60)) $((ELAPSED%60)))

# ------------------------------------------------------------------
# Summary
# ------------------------------------------------------------------
banner "EVALUATION COMPLETE"

echo "Elapsed time : ${ELAPSED_FMT}"
echo "Tasks run    : ${TASK_N}"
echo ""

if [[ ${#FAILED[@]} -gt 0 ]]; then
    echo "❌ FAILED tasks (${#FAILED[@]}):"
    for f in "${FAILED[@]}"; do
        echo "  - $f"
    done
    echo ""
    echo "Check logs in: $LOG_DIR"
    exit 1
else
    if ! $DRY_RUN; then
        echo "✓ All ${TASK_N} tasks completed successfully."
    fi
    echo ""
fi

# Run aggregation if not dry-run
if ! $DRY_RUN; then
    echo "Running aggregation..."
    echo ""
    PYTHONPATH=. "$PYTHON_BIN" scripts/aggregate_paper_stats.py \
        --input-dir "$OUTPUT_BASE" \
        --output-dir "$OUTPUT_BASE"
    echo ""
    echo "Aggregation complete. See:"
    echo "  $OUTPUT_BASE/paper_summary.csv"
    echo "  $OUTPUT_BASE/paper_comparison.csv"
    echo "  $OUTPUT_BASE/paper_stats.json"
fi
